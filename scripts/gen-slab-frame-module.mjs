// Regenerate the backend's bundled default slab frame from the storefront
// asset (public/images/slab-frame.webp). Shipped as a TS module because
// `medusa build` does not copy binary src/ assets into the deployed bundle.
// Usage: node scripts/gen-slab-frame-module.mjs
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const here = path.dirname(fileURLToPath(import.meta.url));
const src = path.join(here, '..', 'public', 'images', 'slab-frame.webp');
const out = path.join(
  here,
  '..',
  'backend',
  'packages',
  'api',
  'src',
  'api',
  'admin',
  'media',
  'slab-frame-default.ts',
);
const b64 = readFileSync(src).toString('base64');
writeFileSync(
  out,
  '// GENERATED by scripts/gen-slab-frame-module.mjs — do not edit by hand.\n' +
    '// Base64 of public/images/slab-frame.webp (the bundled default slab\n' +
    '// frame). Regenerate whenever that asset changes.\n' +
    '// eslint-disable-next-line max-len\n' +
    `export const DEFAULT_SLAB_FRAME_B64 =\n  '${b64}';\n`,
);
console.log(`wrote ${out} (${b64.length} base64 chars)`);
